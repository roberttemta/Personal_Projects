Different SDLC models, such as "Waterfall", "Agile", and "Spiral", offer different approaches to software development. 
  For example, Agile is a flexible and adaptable process that focuses on people and collaboration. 
  Some popular Agile frameworks include:
        -Scrum: Focuses on continuous collaboration, frequent deliveries, and special development cycles called Sprints
        -Kanban: A scheduling system framework that uses cards to track and support the production system
        -Extreme Programming (XP): Focuses on project flexibility and writing high quality, well-tested code 

